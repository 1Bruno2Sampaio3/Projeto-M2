package projetocruzeirao;


public class Campeonato {

    private int ano;
    private double duracao;
    private double peins;
    private double taxa;
    private int cmin, jmin, cmax, jmax;

    public int getAno() {
        return ano;
    }

    public void setAno(int ano) {
        this.ano = ano;
    }

    public double getDuracao() {
        return duracao;
    }

    public void setDuracao(double duracao) {
        this.duracao = duracao;
    }

    public double getPeins() {
        return peins;
    }

    public void setPeins(double peins) {
        this.peins = peins;
    }

    public double getTaxa() {
        return taxa;
    }

    public void setTaxa(double taxa) {
        this.taxa = taxa;
    }

    public int getCmin() {
        return cmin;
    }

    public void setCmin(int cmin) {
        this.cmin = cmin;
    }

    public int getJmin() {
        return jmin;
    }

    public void setJmin(int jmin) {
        this.jmin = jmin;
    }

    public int getCmax() {
        return cmax;
    }

    public void setCmax(int cmax) {
        this.cmax = cmax;
    }

    public int getJmax() {
        return jmax;
    }

    public void setJmax(int jmax) {
        this.jmax = jmax;
    }
    
}
